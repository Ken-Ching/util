package team.kc.util.sqlt.exception;

public class SqlTemplateInitException extends Exception{

	private static final long serialVersionUID = 1L;

	public SqlTemplateInitException() {
		super();
	}

	public SqlTemplateInitException(String message, Throwable cause) {
		super(message, cause);
	}

	public SqlTemplateInitException(String message) {
		super(message);
	}

	public SqlTemplateInitException(Throwable cause) {
		super(cause);
	}

}
